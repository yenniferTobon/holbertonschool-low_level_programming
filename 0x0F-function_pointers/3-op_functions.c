#include <stdlib.h>
#include "3-calc.h"
/**
 * op_add - returns the sum
 * @a: one number
 * @b: two number
 * Return: void
 **/
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - returns the resta
 * @a: one number
 * @b: two number
 * Return: int resta
 **/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - returns the multiplic
 * @a: one number
 * @b: two number
 * Return: int multip
 **/
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - returns the division
 * @a: one number
 * @b: two number
 * Return: int division
 **/
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - returns the modulo
 * @a: one number
 * @b: two number
 * Return: int modulo
 **/
int op_mod(int a, int b)
{
	return (a % b);
}
